// ─────────────────────────────────────────────────────────────────────────

export function permissionsShow() {
  const approvals = readJson(APPROVALS_PATH) ?? DEFAULT_APPROVALS;
  const allowed = readJson(ALLOWED_PATH) ?? {
    bash_commands: [],
    tools: [],
  };

  return {
    approvals,
    approvals_path: APPROVALS_PATH,
    allowed,
    allowed_path: ALLOWED_PATH,
    codex: describeCodex(),
  };
}

