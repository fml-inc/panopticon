interface CodexLocation {
  installed: boolean;
  rules_path: string | null;
  rules_dir: string | null;
}

// Resolve Codex location at call time — `PANOPTICON_CODEX_DIR` env overrides
// the default `~/.codex`. Re-resolved on every call so tests that mutate
// the env var per-case still work.
function locateCodex(): CodexLocation {
  const dir =
    process.env.PANOPTICON_CODEX_DIR ?? path.join(os.homedir(), ".codex");
  if (!fs.existsSync(dir)) {
    return { installed: false, rules_path: null, rules_dir: null };
  }
  const rules_dir = path.join(dir, "rules");
  return {
    installed: true,
    rules_dir,
    rules_path: path.join(rules_dir, "panopticon.rules"),
  };
}

function buildCodexRules(bashCommands: string[], now: string): string {
  const lines = [
    "# Auto-generated by panopticon — do not edit manually.",
    `# Updated: ${now}`,
    "#",
    "# Run /optimize-permissions to regenerate from observed tool usage.",
    "",
  ];
  for (const cmd of bashCommands) {
    const tokens = cmd.split(/\s+/);
    const pattern = tokens.map((t) => JSON.stringify(t)).join(", ");
    lines.push(
      `prefix_rule(pattern = [${pattern}], decision = "allow", justification = "Approved by panopticon")`,
    );
  }
  return `${lines.join("\n")}\n`;
}

// Extract the `git status` / `npx tsup` base command from a rules file line —
// used so we can diff by base command instead of by entire rendered line
// (timestamps in the header would otherwise always diff).
const RULE_PATTERN_RE = /^prefix_rule\(pattern = \[(.+?)\], decision = "allow"/;

function parseCodexRulesBaseCommands(rules: string | null): string[] {
  if (!rules) return [];
  const out: string[] = [];
  for (const line of rules.split("\n")) {
    const m = line.match(RULE_PATTERN_RE);
    if (!m) continue;
    // pattern tokens are JSON-encoded strings separated by ", ".
    const tokens: string[] = [];
    try {
      // Wrap in brackets so JSON.parse treats it as an array.
      const parsed = JSON.parse(`[${m[1]}]`) as unknown[];
      for (const t of parsed) if (typeof t === "string") tokens.push(t);
    } catch {
      continue;
    }
    out.push(tokens.join(" "));
  }
  return out;
}

export interface CodexShowState {
  installed: boolean;
  rules_path: string | null;
  rule_count: number;
}

function describeCodex(): CodexShowState {
  const loc = locateCodex();
  if (!loc.installed || !loc.rules_path) {
    return { installed: false, rules_path: null, rule_count: 0 };
  }
  const rules = readText(loc.rules_path);
  return {
    installed: true,
    rules_path: loc.rules_path,
    rule_count: parseCodexRulesBaseCommands(rules).length,
  };
}

